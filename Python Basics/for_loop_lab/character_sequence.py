name = input()

for ch in name:  # извикване на индексите на name - все едно започва итерация от [0] до края на името
    print(ch)    # в Python индексът може и да започва отзад напред ако започнем от -1 ще извика последната буква,
                 # а -2 предпоследната и така до началото на думата.
